package BasicOfJava;

public class Ternary {
    public static void main(String[] args) {
        
     // ternary operator ? = Return 1 of 2 values if a condtion is true

     // variable = (condition) ? ifTruw : false;

     int score = 99;

     // Instead of if else, we use ternary operator here

     String PassOrFail = (score>=40) ? "Pass" : "Fail";

     System.out.println(PassOrFail);



    }
}
